# Definition
# Assignment operators are used to assign values to variables.


# Basic Assignment

x = 10
print("Value of x:", x)


# Addition Assignment

x = 5
x += 3

print("After += :", x)


# Subtraction Assignment

x = 10
x -= 4

print("After -= :", x)


# Multiplication Assignment

x = 6
x *= 2

print("After *= :", x)


# Division Assignment

x = 20
x /= 4

print("After /= :", x)


# Modulus Assignment

x = 10
x %= 3

print("After %= :", x)


# Floor Division Assignment

x = 15
x //= 2

print("After //= :", x)


# Exponent Assignment

x = 2
x **= 3

print("After **= :", x)


# Intermediate Usage

total = 0

total += 50
total += 30
total += 20

print("Total:", total)


# Advanced Concept

# Updating values dynamically

score = 100

score -= 10
score += 5
score *= 2

print("Final Score:", score)


# Problem Solving Example

# Bank balance update

balance = 5000

deposit = 2000
withdraw = 1500

balance += deposit
balance -= withdraw

print("Final Balance:", balance)


